package models;

public class Book {

    private int id;
    private String title;
    private Author author;

//    hibernate uses when creating class to map to Table
    public Book() {
    }

    public Book(String title, models.Author author) {
        this.title = title;
        this.author = author;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }
}
